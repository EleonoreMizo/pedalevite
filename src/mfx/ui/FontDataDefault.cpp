void	FontDataDefault::make_06x06 (Font &fnt)
{
	const int      w = 6;
	fnt.init (
		_char_per_table,
		w, 6,
		_char_per_row, _char_per_row * w,
		_data_06x06,
		_mapping_8859_15,
		5,
		1
	);
}



